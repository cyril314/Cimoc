    public static String getHeadStr() {
        Random r = new Random(1);
        int i = r.nextInt(HEADERS.length);
        return HEADERS[i];
    }

    public static Request getSimpleMobileRequest(String url) {
        return getMobileRequest(url).build();
    }

    public static Request getMobileRequest(String url, String headerName, String headerVal) {
        return getMobileRequest(url).addHeader(headerName, headerVal).build();
    }

    public static Request getMobileRequest(String url, RequestBody body) {
        return getMobileRequest(url).post(body).build();
    }

    public static Request.Builder getMobileRequest(String url) {
        return new Request.Builder().addHeader("User-Agent", getHeadStr()).url(url);
    }
